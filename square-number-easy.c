#include <stdio.h>

int main()
{   
    /*题目:一个整数,它加上100后是一个完全平方数,
    再加上168又是一个完全平方数,请问该数是多少?*/
    /*x+100==n*n
      x+100+168==m*m
      找到m和n的关系，进而表达出x
      （m+n）*（m-n）==168
      为了使式子更简便 换为i*j==168 */
    int i,j,m,n,x;

    for(i=2;i<=84;i++){
        if(168%i==0){
            j=168/i;
            m=(i+j)/2;
            n=(i-j)/2;
            x=n*n-100;
            if(x>0 && i>j){
                printf("%d\n",x);
            }
            
        }
        
    }


    return 0;
}